// get random icon
func RandomIcon() string {

	return MARKDOWN_ICONS[rand.Intn(len(MARKDOWN_ICONS))]
}

// get random icon without repeating the same icon twice
func RandomIconWithBuffer(lastIcons []string) string {

	if len(lastIcons) >= len(MARKDOWN_ICONS) {
		return ""
	}

	icon := RandomIcon()
	for _, lastIcon := range lastIcons {
		for lastIcon == icon {
			icon = RandomIcon()
		}
	}
	return icon
}
